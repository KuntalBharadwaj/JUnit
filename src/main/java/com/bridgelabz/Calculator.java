package com.bridgelabz;

public class Calculator {
    int addTwoNumber(int a, int b) {
        return a+b;
    }

    int subtractTwoNumber(int a, int b) {
        return a - b;
    }

    int multiplyTwoNumber(int a, int b) {
        return a * b;
    }

    int divideTwoNumber(int a, int b) {
        return a / b;
    }


}
